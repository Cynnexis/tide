/// Returns the language full name of the given [languageCode]
/// (encoded as two-letters format, such as 'en' or 'fr-CA', etc.).
///
/// If the language is not found, the parameter [languageCode] is
/// returned.
String getLanguageFromCode(String languageCode) {
  if (languageCode.length < 2) {
    throw ArgumentError("The given languageCode must be at least 2 "
        "characters long. Current value: \"$languageCode\" "
        "(length: ${languageCode.length})");
  }

  Map<String, dynamic>? lang = _isoLang[languageCode.substring(0, 2)];

  // If not found, return the parameter languageCode
  if (lang == null) return languageCode;

  String fullName = "";
  if (lang["name"] != null &&
      lang["name"] is String &&
      lang["name"].isNotEmpty) {
    fullName += lang["name"];
  }

  if (lang["nativeName"] != null &&
      lang["nativeName"] is String &&
      lang["nativeName"].isNotEmpty) {
    if (fullName.isNotEmpty) {
      fullName += " (${lang["nativeName"]})";
    } else {
      fullName += lang["nativeName"];
    }
  }

  return fullName.isNotEmpty ? fullName : languageCode;
}
